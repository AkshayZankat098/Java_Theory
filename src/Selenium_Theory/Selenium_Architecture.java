package Selenium_Theory;

public class Selenium_Architecture {

// Language Supported :: Java, Python, C# (.NET), JavaScript / Node.js, Ruby, Kotlin / Scala
// Browser Supported  :: Google Chrome (Chromedriver), Mozilla Firefox (GeckoDriver), Microsoft Edge (EdgeDriver), Safari (Safari-driver (Built-in)), Internet Explorer (IEDriverServer) )

// Selenium Architecture Roadmap Flow ::
// 1) Selenium code is written in a test java file inside IDE (Test.java).
//    This code is translated into JSON format by the Selenium client library.

// 2) Using JSON over HTTP protocol, The Selenium client library sends the commands to the browser drivers (Chromedriver)
// 3) The respective browser driver (chromedriver) communicates with the actual browser to execute the commands.
// 4) After action is run successfully browser responds to the browser driver and then to via JSON or http protocol our IDE


}
